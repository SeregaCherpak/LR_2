package abs.servlet;

public class Car {
    private String mark;
    private String model;
    private String price;
    private String year;
    private String engine_capacity;

    public Car(String mark, String model, String price, String year, String engine_capacity) {
        this.mark = mark;
        this.model = model;
        this.price = price;
        this.year = year;
        this.engine_capacity = engine_capacity;
    }
}
